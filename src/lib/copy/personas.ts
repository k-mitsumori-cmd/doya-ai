import type { PersonaData } from './gemini'

/**
 * リッチペルソナ: 基本PersonaDataに深層心理・行動文脈を追加
 */
export interface RichPersonaData extends PersonaData {
  // 深層心理
  innerVoice: string[]
  frustrations: string[]
  aspirations: string[]
  fears: string[]

  // 行動コンテキスト
  dailyScene: string
  decisionProcess: string
  currentAlternatives: string[]
  alternativeDissatisfaction: string[]

  // 購買心理
  objections: string[]
  reassurances: string[]
  triggerMoments: string[]
  resonatingMessages: string[]
}

/**
 * リッチペルソナ → Geminiプロンプト用テキストに変換
 */
export function richPersonaToPromptBlock(persona: RichPersonaData): string {
  const lines = [
    `${persona.name}（${persona.age}、${persona.gender}、${persona.occupation}）`,
    `年収: ${persona.income}`,
    '',
    '【悩み・課題】',
    ...persona.painPoints.map((p, i) => `${i + 1}. ${p}`),
    '',
    '【心の声・本音】',
    ...persona.innerVoice.map(v => `- 「${v}」`),
    '',
    '【フラストレーション】',
    ...persona.frustrations.map(f => `- ${f}`),
    '',
    '【本当に欲しい未来】',
    ...persona.aspirations.map(a => `- ${a}`),
    '',
    '【恐れ・不安】',
    ...persona.fears.map(f => `- ${f}`),
    '',
    '【日常の一場面】',
    persona.dailyScene,
    '',
    '【今使っている代替手段 → 不満】',
    ...persona.currentAlternatives.map((alt, i) =>
      `- ${alt} → ${persona.alternativeDissatisfaction[i] || '不満あり'}`
    ),
    '',
    '【購買を決めるトリガー瞬間】',
    ...persona.triggerMoments.map(t => `- ${t}`),
    '',
    '【刺さるメッセージ】',
    ...persona.resonatingMessages.map(m => `- 「${m}」`),
    '',
    '【購買の障壁 → 解消ポイント】',
    ...persona.objections.map((obj, i) =>
      `- 障壁: ${obj} → 解消: ${persona.reassurances[i] || ''}`
    ),
    '',
    `検索行動: ${persona.searchBehavior}`,
    `購買トリガー: ${persona.purchaseTrigger}`,
  ]
  return lines.join('\n')
}

