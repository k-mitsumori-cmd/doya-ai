// ============================================
// ドヤカンニング 質問検出（ヒューリスティック）
// ============================================
// finalセグメントが「相手からの質問/依頼」かを軽量判定。雑談・相づちはスキップ。
// LLM分類は回答生成と統合せずレイテンシを抑えるため、まずはヒューリスティックで足切りする。

const QUESTION_ENDINGS = [
  'ですか', 'ますか', 'でしょうか', 'のですか', 'んですか', 'かな', 'かね',
  'ください', 'いただけますか', 'もらえますか', '教えて', 'どう', 'いかが',
  'なぜ', 'どうして', 'どのように', 'どれくらい', 'いくら', 'いつ', 'どこ', 'だれ', '誰',
  '何', 'なに', 'なん', 'ありますか', 'できますか',
]

const SMALL_TALK = ['はい', 'ええ', 'うん', 'なるほど', 'そうですね', 'ありがとう', 'お願いします', 'よろしく']

/** ざっくり「質問っぽいか」を判定。閾値は recall 重視（取りこぼしを避ける）。 */
export function looksLikeQuestion(text: string): boolean {
  const t = (text || '').trim()
  if (t.length < 4) return false
  // 相づち・短い同意だけのセグメントは除外
  if (SMALL_TALK.some((s) => t === s || t === s + '。')) return false
  if (t.includes('?') || t.includes('？')) return true
  return QUESTION_ENDINGS.some((kw) => t.includes(kw))
}
