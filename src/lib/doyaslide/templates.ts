// ============================================
// ドヤスライド 資料タイプ別 構成ひな型
// ============================================
// docType ごとに「どんな流れのスライド構成にするか」をAIに伝えるためのガイド。
import type { DocType } from './types'

export const STRUCTURE_TEMPLATES: Record<DocType, string> = {
  sales:
    '営業資料。流れ: 表紙 → 顧客の課題 → 解決策（自社サービス） → ベネフィット → 導入実績/事例 → 料金/プラン → 行動喚起(CTA)。信頼感があり力強いトーン。',
  proposal:
    '提案資料。流れ: 表紙 → 背景 → 現状の課題 → 提案内容 → 期待される効果 → スケジュール → 費用 → まとめ/CTA。論理的で誠実なトーン。',
  sns:
    'SNS用資料（カルーセル想定）。流れ: 強いフック（1枚目で目を引く） → 要点1 → 要点2 → 要点3 → まとめ → フォロー/CTA。短い言葉・大きな文字・派手でスクロールを止める作り。',
  seminar:
    'セミナー・登壇資料。流れ: タイトル → 自己紹介 → アジェンダ → 各トピック（複数枚） → まとめ → 質疑/連絡先。親しみやすく分かりやすいトーン。',
  recruit:
    '採用資料。流れ: 表紙 → 会社の魅力 → 事業内容 → 働く環境/カルチャー → 社員の声 → 募集要項 → 応募導線。温かく前向きなトーン。',
  pitch:
    'ピッチ資料。流れ: 表紙 → 課題 → ソリューション → 市場規模 → トラクション/実績 → ビジネスモデル → チーム → 資金使途/Ask。攻めて印象に残るトーン。',
  internal:
    '社内共有資料。流れ: 表紙 → 目的/背景 → 現状 → ポイント（複数枚） → アクション → まとめ。簡潔で分かりやすいトーン。',
  custom:
    'ユーザーが指定した目的に最適な構成をあなたが設計する。1枚目は必ず強い表紙、最後は必ずまとめ/CTA。',
}

export function getStructureTemplate(docType: string): string {
  return STRUCTURE_TEMPLATES[(docType as DocType)] || STRUCTURE_TEMPLATES.proposal
}
